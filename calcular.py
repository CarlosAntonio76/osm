class Calculo:
    def soma(x, Y):
        return x + Y
    
    def multiplica(x, y):
        return x * y
    
    def divisao(x, y):
        return x / y
    
    def subtrai(x, y):
        return x - y
    
       
calcular = Calculo


print(calcular.soma(1,5))
print(calcular.multiplica(5,5))
print(calcular.divisao(8,4))
print(calcular.subtrai(10,8))
